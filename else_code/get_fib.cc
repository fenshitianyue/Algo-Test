///////////////////////////////////////////////////////
// 题目：
//    获取第n个斐波那契数的后6位，如果第n个数小于6位，
//    则全部输出
///////////////////////////////////////////////////////

#include<stdio.h>

int main(){
	int n;
	int table[100001] = { 1, 2 };
	for (int i = 2; i < 100001; i++)
		table[i] = (table[i - 1] % 1000000 + table[i - 2] % 1000000) % 1000000;
	while (~scanf("%d", &n))
	{
		if(n<25)
			printf("%d\n", table[n - 1]);
		else
			printf("%6d\n", table[n - 1]);
	}
	return 0;
}
